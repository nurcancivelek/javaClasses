package com.class3;

public class ShorthandOperators {
	
	public static void main(String[] args) {
		int num2=10;
		num2=num2-5;
		System.out.println(num2);
		
		int num4=50; //num4=num4=20
			num4+=20;
		System.out.println(num4);
		
		num4-=30;// num4=70-30
		System.out.println(num4);
		
		num4/=10;//num4=40/10
		System.out.println(num4);
		
		num4%=2; //num4=4%2
		
	System.out.println(num4);
	
	int num5= 15;
	num5+=100;
	System.out.println(num5);
	
	int num6=100;
	num6-=67;
	System.out.println(num6);
	
	float cakePiece=11.0f;
	cakePiece/=4;
	System.out.println(cakePiece);
	
	float cakePiece2=25.0f;
	cakePiece2/=7;
	System.out.println(cakePiece2);
	
	

	
	
	
	
	
	
	
	
			
		
		
		}

}
